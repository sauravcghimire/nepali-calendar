import Foundation

enum DateConverter {
    // BS 2000-01-01 = AD 1943-04-14
    private static let referenceAD = DateComponents(
        calendar: Calendar(identifier: .gregorian),
        timeZone: TimeZone(identifier: "UTC"),
        year: 1943, month: 4, day: 14
    ).date!
    private static let referenceYear = 2000

    static func adToBS(year: Int, month: Int, day: Int) -> (year: Int, month: Int, day: Int)? {
        var cal = Calendar(identifier: .gregorian)
        cal.timeZone = TimeZone(identifier: "UTC")!
        guard let adDate = cal.date(from: DateComponents(year: year, month: month, day: day)) else {
            return nil
        }
        let totalDaysRaw = cal.dateComponents([.day], from: referenceAD, to: adDate).day ?? 0
        if totalDaysRaw < 0 { return nil }
        var remaining = totalDaysRaw

        var bsYear = referenceYear
        while remaining > 0 {
            guard let months = bsMonthDays[bsYear] else { return nil }
            let daysInYear = months.reduce(0, +)
            if remaining >= daysInYear {
                remaining -= daysInYear
                bsYear += 1
                continue
            }
            for m in 0..<12 {
                if remaining >= months[m] {
                    remaining -= months[m]
                } else {
                    return (bsYear, m + 1, remaining + 1)
                }
            }
        }
        return (bsYear, 1, 1)
    }

    static func bsToAD(year: Int, month: Int, day: Int) -> (year: Int, month: Int, day: Int)? {
        guard bsMonthDays[year] != nil, month >= 1, month <= 12 else { return nil }
        guard let months = bsMonthDays[year], day >= 1, day <= months[month - 1] else { return nil }

        var totalDays = 0
        for y in referenceYear..<year {
            guard let m = bsMonthDays[y] else { return nil }
            totalDays += m.reduce(0, +)
        }
        if let months = bsMonthDays[year] {
            for m in 0..<(month - 1) {
                totalDays += months[m]
            }
        }
        totalDays += day - 1

        var cal = Calendar(identifier: .gregorian)
        cal.timeZone = TimeZone(identifier: "UTC")!
        guard let result = cal.date(byAdding: .day, value: totalDays, to: referenceAD) else {
            return nil
        }
        let comps = cal.dateComponents([.year, .month, .day], from: result)
        return (comps.year!, comps.month!, comps.day!)
    }

    static func daysInBSMonth(year: Int, month: Int) -> Int? {
        guard let months = bsMonthDays[year], month >= 1, month <= 12 else { return nil }
        return months[month - 1]
    }

